DROP DATABASE IF EXISTS JOB;
CREATE DATABASE JOB;
USE JOB;

DROP TABLE IF EXISTS EID;
CREATE TABLE EID(
    EID VARCHAR(20),
    PRIMARY KEY (EID)
);

DROP TABLE IF EXISTS Individual;
CREATE TABLE Individual(
    IEID VARCHAR(20),
    INAME VARCHAR(100),
    ILOCATION VARCHAR(1000),
    PRIMARY KEY(IEID),
    FOREIGN KEY (IEID) REFERENCES EID(EID)
);

DROP TABLE IF EXISTS Organization;
CREATE TABLE Organization(
    OEID VARCHAR(20),
    ONAME VARCHAR(100),
    OLOCATION VARCHAR(1000),
    PRIMARY KEY (OEID),
    FOREIGN KEY (OEID) REFERENCES EID(EID)
);

DROP TABLE IF EXISTS Employment_Information;
CREATE TABLE Employment_Information(
    ID VARCHAR(20),
    EID VARCHAR(20),
    NAME VARCHAR(100),
    SALARY VARCHAR(50),
    LOCATION VARCHAR(1000),
    POSITION VARCHAR(1000),
    DESCRIPTION VARCHAR(1000),
    REQUIREMENT VARCHAR(1000),
    BENEFIT VARCHAR(1000),
    QUANTITY INT,
    PRIMARY KEY(ID),
    FOREIGN KEY (EID) REFERENCES EID(EID)
);